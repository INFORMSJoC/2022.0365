cost01(:,:,:,n1)=repmat(h(:,n1),1,n3,n2).*(ytrain(:,:,:,n1)-y(:,:,:,n1))+repmat(c(:,:,n1),1,1,n2).*ytrain(:,:,:,n1);
cost01(:,:,:,n1)=cost01(:,:,:,n1).*s;
