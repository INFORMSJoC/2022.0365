     cost1(:,:,nn2,tt)=cost1(:,:,nn2,tt).*s;
     s=((sign(-ytest(:,:,:,tt)+ss+yy(:,:,:,tt))+1)./2);
     cost2(:,:,nn2,tt)=d(1)*repmat(p(:,tt),1,n3,nn2).*(-ytest(:,:,:,tt)+ss+yy(:,:,:,tt))+repmat(c(:,:,tt),1,1,nn2).*ytest(:,:,:,tt);
     cost2(:,:,nn2,tt)=cost2(:,:,nn2,tt).*s;
     cost(:,:,:,tt)=cost1(:,:,:,tt)+cost2(:,:,:,tt);
end
