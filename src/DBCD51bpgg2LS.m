cost2(:,:,nn2,n1)=d(n1)*repmat(p(:,n1),1,n3,nn2).*(-ytest(:,:,:,n1)+yy(:,:,:,n1))+repmat(c(:,:,n1),1,1,nn2).*ytest(:,:,:,n1);
cost2(:,:,nn2,n1)=cost2(:,:,nn2,n1).*s;
