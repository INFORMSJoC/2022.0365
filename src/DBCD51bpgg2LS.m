    s0=0;
     for t=tt+1:n1
         y1=y(:,:,:,t)-ytrain(:,:,:,t);
         s0=s0+((sign(y1)+1)./2).*y1;
         y2=yy(:,:,:,t)-ytest(:,:,:,t);
         s1=s1+((sign(y2)+1)./2).*y2;
     end
     ss0=s0;
     ss=s1;
     mae0(:,:,:,tt)=abs(ytrain(:,:,:,tt)-ss0-y(:,:,:,tt));
     sl0(:,:,:,tt)=max(min(ytrain(:,:,:,tt),y(:,:,:,tt)),0)./(y(:,:,:,tt)+(sign(y(:,:,:,tt))-1)*(-exp(100)));
     mse0(:,:,:,tt)=mae0(:,:,:,tt).^2;
