     mse(:,:,:,tt)=mae(:,:,:,tt).^2;
     s=((sign(ytrain(:,:,:,1)-ss0-y(:,:,:,1))+1)./2);
     cost01(:,:,:,tt)=repmat(h(:,tt),1,n3,n2).*(ytrain(:,:,:,tt)-ss0-y(:,:,:,tt))+repmat(c(:,:,tt),1,1,n2).*ytrain(:,:,:,tt);
